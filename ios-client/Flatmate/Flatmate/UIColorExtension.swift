import Foundation
import UIKit

extension UIColor{
    
    
    class var grayBackground: UIColor {
        return UIColor(red: 240/255, green: 245/255, blue: 246/255, alpha: 1.0)
    }

    class var darkBlue: UIColor {
        return UIColor(red: 116/255, green: 125/255, blue: 226/255, alpha: 1.0)
    }
    
    class var brightBlue: UIColor {
        return UIColor(red: 116/255, green: 222/255, blue: 226/255, alpha: 1.0)
    }
    
    class var grayTintColor: UIColor {
        return UIColor(red: 164/255, green: 170/255, blue: 171/255, alpha: 1.0)
    }
    
    class var darkTintColor: UIColor {
        return UIColor(red: 85/255, green: 97/255, blue: 100/255, alpha: 1.0)
    }
    
    class var tfBackgroundColor: UIColor {
        return UIColor(red: 0/255, green: 0/255, blue: 0/255, alpha: 0.05)
    }
    
    class var startSwipingColor: UIColor {
        return UIColor(red: 85/255, green: 97/255, blue: 100/255, alpha: 1.0)
    }
}
